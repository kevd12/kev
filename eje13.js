let numero = 10; // Inicializamos la variable con el número desde donde empieza la cuenta regresiva

console.log('Cuenta regresiva:'); // Mostramos un mensaje de inicio en la consola

while (numero >= 0) {           // Mientras el número sea mayor o igual a 0...
  console.log(numero);          // ...lo imprimimos en la consola
  numero--;                     // ...y luego lo decrementamos en 1 (restamos 1)
}
